// 获取所有等级
export const levels = Object.keys(vocabularyData);

// 获取特定等级的所有单词
export const getWordsByLevel = (level) => {
  return vocabularyData[level]?.words || [];
};

// 获取所有单词（扁平化）
export const getAllWords = () => {
  return levels.flatMap((level) => vocabularyData[level].words);
};

// 根据ID获取单词
export const getWordById = (id) => {
  return getAllWords().find((word) => word.id === id);
};

// 随机获取单词
export const getRandomWords = (level, count = 5) => {
  const words = getWordsByLevel(level);
  const shuffled = [...words].sort(() => 0.5 - Math.random());
  return shuffled.slice(0, count);
};
